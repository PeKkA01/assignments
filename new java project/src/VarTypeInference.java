public class VarTypeInference {
    public static void main(String[] args) {
        var message = "Hello, Java 10+!";
        var count = 100;
        var pi = 3.14159;
        System.out.println("Message: " + message);
        System.out.println("Count: " + count);
        System.out.println("PI: " + pi);
    }
}