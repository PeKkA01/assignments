public class CharVsString {
    public static void main(String[] args) {
        char singleCharacter = 'C';
        String sequenceOfCharacters = "Programming";

        System.out.println("char data type: " + singleCharacter);
        System.out.println("String data type: " + sequenceOfCharacters);
        System.out.println("Length of the String: " + sequenceOfCharacters.length());
    }
}